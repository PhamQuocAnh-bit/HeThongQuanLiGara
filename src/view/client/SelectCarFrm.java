    public SelectCarFrm( JFrame parent ,Client cl) {
        
        initComponents();
        this.setLocationRelativeTo(null);
        
        CarDAO dao = new CarDAO() ;
        ArrayList<Car>  listCar = dao.searchCarOfClient(String.valueOf(cl.getId())) ;
        DefaultTableModel tblModel = (DefaultTableModel) tblCar.getModel() ;
        tblModel.setRowCount(0);
        txtIdclient.setText(String.valueOf(cl.getId())) ;
        int stt = 1 ;
        for (Car ca : listCar) {
            Object dataRow[] = new Object[6] ;
            
            dataRow[0] = stt ;
            dataRow[1] = ca.getId() ;
            dataRow[2] = ca.getName() ;
            dataRow[3] = ca.getBrand();
            dataRow[4] = ca.getPlate() ;
            dataRow[5] = ca.getNote() ;
            stt++ ;
            
            tblModel.addRow(dataRow);
        }
        
        
    }
    
    
   

    
    
    
    
