    private void tblCarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCarMouseClicked
        
        int selectRow = tblCar.getSelectedRow() ;
        if(selectRow != -1) {
            
           txtName.setText(tblCar.getValueAt(selectRow,2).toString());
           txtBrand.setText( tblCar.getValueAt(selectRow,3).toString());
           txtID.setText( tblCar.getValueAt(selectRow,1).toString());
           txtPlate.setText(tblCar.getValueAt(selectRow,4).toString());
          txtNote.setText(tblCar.getValueAt(selectRow,5).toString());
           
           
           
          
           
            
        }
    }//GEN-LAST:event_tblCarMouseClicked
    public Car getSelectCar(){
        Car ca = new Car() ;
         ca.setId(Integer.parseInt(txtIdclient.getText()));
        ca.setId(Integer.parseInt(txtID.getText()));
        ca.setName(txtName.getText());
        ca.setBrand(txtBrand.getText());
        ca.setPlate(txtPlate.getText());
        ca.setNote(txtNote.getText());
        
        return ca ;
         
        
    }
   
    
