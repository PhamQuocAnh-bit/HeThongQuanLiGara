    public void showClient(){
         ClientDAO dao = new ClientDAO() ;
         ArrayList<Client> client = dao.searchClientByName("") ;
        DefaultTableModel tbModel = (DefaultTableModel) tblClient.getModel();
        tbModel.setRowCount(0);
        int stt = 1 ;
        for (Client cl : client){
            Object dataRow[] =  new Object[9];
            dataRow[0] = stt ;
            dataRow[1] = cl.getId();
            dataRow[2] = cl.getFullname() ;
            dataRow[3] = cl.getEmail() ;
            dataRow[4] = cl.getGender();
            dataRow[5] = cl.getIdcard();
            dataRow[6] = cl.getTel() ;
            dataRow[7] = cl.getAddress() ;
            dataRow[8] = cl.getNote() ;
            stt++ ;
            tbModel.addRow(dataRow);
          
            
        }
               
        
        
    }
