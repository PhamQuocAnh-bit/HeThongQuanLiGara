    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        ClientDAO dao = new ClientDAO();
        String name = txtName.getText() ;
        int stt = 1 ;
        if ( name.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Vui L√≤ng Nh·∫≠p T√™n ƒê·ªÉ T√¨m Ki√™m üòÄ");
        }
        else {
            ArrayList<Client> client = dao.searchClientByName(name) ;
            DefaultTableModel tblModel = (DefaultTableModel) tblClient.getModel();
            tblModel.setRowCount(0);
            
            for (Client cl : client ) {
                Object dataRow[] = new Object[9] ;
                
                dataRow[0] = stt ;
                dataRow[1] = cl.getId();
                dataRow[2] = cl.getFullname();
                dataRow[3] = cl.getEmail();
                dataRow[4] = cl.getGender();
                dataRow[5] = cl.getIdcard();
                dataRow[6] = cl.getTel();
                dataRow[7] = cl.getAddress() ;
                dataRow[8] =cl.getNote();
                stt++ ;
                tblModel.addRow(dataRow);
                
                       
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

